public class Objective8Lab1 {
  public static void main(String[] args) {
    
    // a new range from 1-10-1 up and down.
    // the up 1-10
    for (int increaseCounter = 1; increaseCounter <= 10; increaseCounter ++) {
      System.out.println(increaseCounter);
    }
    // the down 10-1
    for (int decreaseCounter = 10; decreaseCounter >= 1; decreaseCounter --) {
      System.out.println(decreaseCounter);
    }
  }
}